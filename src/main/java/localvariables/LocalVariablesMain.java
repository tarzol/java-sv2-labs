package localvariables;

public class LocalVariablesMain {

    public static void main(String[] args) {

        boolean b;
        //System.out.println("b értéke: "+b);
        b = true;
        System.out.println("b értéke: "+b);

        int number = 2;
        System.out.println(("number értéke: "+number));

        int i = 3;
        int j = 4;
        int k = i;

        System.out.println(("i értéke: "+i));
        System.out.println(("j értéke: "+j));
        System.out.println(("k értéke: "+k));

        String s = "Hello World";
        String t = s;
        System.out.println(("s értéke: "+s));
        System.out.println(("t értéke: "+t));

        int x = 0;
        System.out.println(("x értéke: "+x));

    }

    //System.out.println(("x értéke: "+x));


}
