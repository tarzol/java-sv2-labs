package math.math;

public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.max(4, 7));
        System.out.println(Math.min(4, 7));
        System.out.println(3.14);
        System.out.println(Math.round(3.14));
        System.out.println(Math.round(2.24));
        System.out.println(Math.abs(-7));
        System.out.println(Math.subtractExact(9,3));
        System.out.println(Math.multiplyExact(9,3));
        System.out.println(Math.pow(3,4));
        System.out.println(Math.incrementExact(9));
        System.out.println(Math.decrementExact(9));
        System.out.println(Math.random());
    }
}
